<script>
document.addEventListener('DOMContentLoaded', function () {
    // 1. Lógica del Modal
    var modalElement = document.getElementById('modalBusquedaArticulo');
    if (modalElement) {
        var modal = bootstrap.Modal.getInstance(modalElement);
        if (!modal) new bootstrap.Modal(modalElement);
    }
    var card = document.querySelector('[data-bs-target="#modalBusquedaArticulo"]');
    if (card) {
        card.addEventListener('click', function (e) {
            e.preventDefault();
            new bootstrap.Modal(document.getElementById('modalBusquedaArticulo')).show();
        });
    }

    // 2. ACTIVAR TOOLTIPS PREMIUM (El globo flotante)
    const tooltipElements = document.querySelectorAll('.menu-ini-item, .menu-ini-section-label');
    tooltipElements.forEach(el => {
        if (el.getAttribute('title')) {
            new bootstrap.Tooltip(el, {
                delay: { "show": 250, "hide": 50 }, // Pequeño retraso para que no salte de inmediato
                placement: 'bottom', // Abajo de la tarjeta para no tapar el ícono
                animation: true
            });
        }
    });
});
</script>

